    def NetworkIPScan(self):
        from networkscan import Networkscan
        from nmb.NetBIOS import NetBIOS    # Get NetBIOS hostname
        from getmac import get_mac_address  # Get mac address from ip
        if self.lineEdit.text():
            self.tableWidget.setRowCount(0) # Clear table on each new Scan
            my_network = self.lineEdit.text()
            my_scan = Networkscan(my_network)
            my_scan.run()
            for i in my_scan.list_of_hosts_found:
                mac = get_mac_address(ip=i)
                try:
                    hostname = NetBIOS().queryIPForName(ip=i, timeout=1)[0]
                except Exception:
                    hostname = i
                # necessary even when there are no rows in the table
                currentRowCount = self.tableWidget.rowCount()
                self.tableWidget.insertRow(currentRowCount)
                self.tableWidget.setItem(currentRowCount, 0, QTableWidgetItem(i))
                self.tableWidget.setItem(currentRowCount, 1, QTableWidgetItem(hostname))
                self.tableWidget.setItem(currentRowCount, 2, QTableWidgetItem(mac))
            self.ScanButtonIPscanner.setEnabled(True)
            self.lineEdit.setEnabled(True)
            self.PauseBottonIPscanner.setEnabled(False)


    def startscan(self):
        if self.lineEdit.text():
            self.ScanButtonIPscanner.setEnabled(False)
            self.PauseBottonIPscanner.setEnabled(True)
            self.lineEdit.setEnabled(False)
            self.scanner_th = NetworkScannerThread(self.NetworkIPScan)
            self.threadpool.start(self.scanner_th)

    def stopscan(self):
        self.ScanButtonIPscanner.setEnabled(True)
        self.lineEdit.setEnabled(True)
        self.PauseBottonIPscanner.setEnabled(False)
        self.scanner_th.stop()

